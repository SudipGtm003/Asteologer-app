let currentLang = localStorage.getItem('site_lang') || 'en';

function updateLanguage(lang) {
  currentLang = lang;
  localStorage.setItem('site_lang', lang);
  
  const langBtn = document.getElementById('langBtn');
  if (langBtn) {
    langBtn.textContent = lang === 'ne' ? 'EN' : 'नेपाली';
  }

  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.getAttribute('data-i18n');
    if (translations[lang] && translations[lang][key]) {
      if (el.tagName === 'INPUT' || el.tagName === 'TEXTAREA') {
         el.setAttribute('placeholder', translations[lang][key]);
      } else {
         el.innerHTML = translations[lang][key];
      }
    }
  });
}

function createStars() {
    let container = document.getElementById('stars-container');
    if (!container) {
        container = document.createElement('div');
        container.id = 'stars-container';
        container.className = 'stars';
        document.body.prepend(container);
    }
    const count = 150; 
    for (let i = 0; i < count; i++) {
        const star = document.createElement('div');
        star.className = 'star';
        const x = Math.random() * 100;
        const y = Math.random() * 100;
        const size = Math.random() * 2 + 0.5;
        const duration = Math.random() * 3 + 2;
        star.style.left = `${x}%`;
        star.style.top = `${y}%`;
        star.style.width = `${size}px`;
        star.style.height = `${size}px`;
        star.style.setProperty('--duration', `${duration}s`);
        container.appendChild(star);
    }
}

function initReveal() {
  const observer = new IntersectionObserver((entries) => {
    entries.forEach(entry => {
      if (entry.isIntersecting) {
        entry.target.classList.add('active');
      }
    });
  }, { threshold: 0.1 });

  document.querySelectorAll('.reveal, .card, .section, .box').forEach(el => {
    if (!el.classList.contains('reveal')) el.classList.add('reveal');
    observer.observe(el);
  });
}

function toggleLanguage() {
  const newLang = currentLang === 'ne' ? 'en' : 'ne';
  updateLanguage(newLang);
}

function toggleMenu() {
  const body = document.body;
  const nav = document.querySelector('nav');
  const isOpen = body.classList.toggle('menu-open');
  if (nav) nav.classList.toggle('open');
}

function closeMenu() {
  document.body.classList.remove('menu-open');
  const nav = document.querySelector('nav');
  if (nav) nav.classList.remove('open');
}

